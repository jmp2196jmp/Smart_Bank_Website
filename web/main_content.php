          <p align="justify">Online banking, also known as internet banking, e-banking, or virtual banking, is an electronic payment system that enables customers of a bank or other financial institution to conduct a range of financial transactions through the financial institution's website. The online banking system will typically connect to or be part of the core banking system operated by a bank and is in contrast to branch banking that was the traditional way customers access banking services. Fundamentally and in mechanism, online banking, internet banking and e-banking are the same thing. To access a financial institution's online banking facility, a customer with internet access would need to register with the institution for the service, and set up a password and other credentials for customer verification. </p>
          <p>Financial institutions now routinely allocate customers numbers, whether or not customers have indicated an intention to access their online banking facility. Customers' numbers are normally not the same as account numbers, because a number of customer accounts can be linked to the one customer number. The customer number can be linked to any account that the customer controls, such as cheque, savings, loan, credit card and other accounts.</p>
